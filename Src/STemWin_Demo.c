/***************************************************************************************
  * @brief   获取角度,这里只是模拟。在实际使用中这个角度应该是一个实际值
  * @input
  * @return
***************************************************************************************/
static float _GetAngle(int tDiff) {
	if (tDiff < 15000) {
		return  225 - 0.006 * tDiff ;
	}
	tDiff -= 15000;
	if (tDiff < 7500) {
		return  225 - 90 + 0.012 * tDiff ;
	}
	return 225;
}

