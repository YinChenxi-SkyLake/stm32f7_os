/***************************************************************************************
  * @brief   绘制2D图形
  * @input
  * @return
***************************************************************************************/
void draw_2d(void)
{
    GUI_SetBkColor(GUI_BLUE); //设置背景颜色
    GUI_Clear();

    //绘制用水平颜色梯度填充的矩形
    GUI_DrawGradientH(55, 25, 105, 75, 0X4117BB, 0XC6B6F5);
    //绘制用垂直颜色梯度填充的矩形
    GUI_DrawGradientV(110, 25, 160, 75, 0X4117BB, 0XC6B6F5);
    //绘制水平颜色梯度填充的圆角矩形
    GUI_DrawGradientRoundedH(165, 25, 215, 75, 12, 0X4117BB, 0XC6B6F5);
    //绘制垂直颜色梯度填充的圆角矩形
    GUI_DrawGradientRoundedV(220, 25, 270, 75, 12, 0X4117BB, 0XC6B6F5);
    GUI_DrawRect(275, 25, 325, 75); //在当前窗口中指定的位置绘制矩形
    GUI_FillRect(330, 25, 380, 75); //在当前窗口中指定位置绘制填充的矩形区域
    GUI_SetPenSize(5); //设置画笔颜色,单位像素点
    GUI_DrawLine(5, 80, 55, 130); //绘制线条
    for(uint8_t i = 0; i < 25; i += 3)
        GUI_DrawCircle(100, 110, i); //绘制圆，无法绘制半径大于 180 的圆
    //在指定位置绘制指定尺寸的填充的圆,无法绘制半径大于 180 的圆
    GUI_FillCircle(160, 110, 25);
    GUI_SetPenSize(2);
    GUI_SetColor(GUI_CYAN);
    //在指定位置绘制指定尺寸的椭圆的轮廓 无法处理大于 180 的 rx/ry 参数
    GUI_DrawEllipse(225, 110, 35, 25);
    GUI_SetColor(GUI_MAGENTA);
    GUI_FillEllipse(225, 110, 30, 20); //在指定位置绘制指定尺寸的填充的椭圆
    
    /*------------------绘制波形线---------------------------*/
    I16 aY[200];
    int i;
    for(i=0;i<GUI_COUNTOF(aY);i++) 
        aY[i] = rand()%100;
    GUI_ClearRect(0,150,200,200); //每次显示前先清除显示区域
    GUI_DrawGraph(aY,GUI_COUNTOF(aY),0,150);

    /*------------------绘制仪表---------------------------*/
    char ac[4];
    int x0 = 350;
    int y0 = 220;
    GUI_SetPenSize(3);
    GUI_SetTextMode(GUI_TM_TRANS);
    GUI_SetFont(&GUI_FontComic18B_ASCII);
    GUI_SetColor( GUI_BLACK );
    GUI_DrawArc( x0, y0, 100, 100, -30, 210 );//绘制弧线
    GUI_Delay(1000);
    for (uint8_t i = 0; i <= 23; i++) {
        float a = (-30 + i * 10) * 3.1415926 / 180;
        int x = -91 * cos(a) + x0;
        int y = -91 * sin(a) + y0;
        if (i % 2 == 0)
            GUI_SetPenSize( 4 );
        else
            GUI_SetPenSize( 2 );
        GUI_DrawPoint(x, y);
        if (i % 2 == 0) {
            x = -80 * cos(a) + x0;
            y = -80 * sin(a) + y0;
            sprintf(ac, "%d", 10 * i);
            GUI_SetTextAlign(GUI_TA_VCENTER);
            GUI_DispStringHCenterAt(ac, x, y);
        }
    }
    
}

